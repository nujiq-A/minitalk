#include "libft/libft.h"
#include "libft/ft_printf/ft_printf.h"
#include <signal.h>
#include <unistd.h>

void	ft_btoa(int sig, siginfo_t *info, void *context)
{
	static unsigned char	c = 0;
	static int				bit = 0;

	(void)context;
	if (sig == SIGUSR1)
		c |= 1;
	if (++bit == 8)
	{
		bit = 0;
		if (!c)
			kill(info->si_pid, SIGUSR2);
		write(1, &c, 1);
		c = 0;
	}
	else
		c <<= 1;
}

